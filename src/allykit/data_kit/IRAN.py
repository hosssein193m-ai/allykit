def search_city(query: str) -> dict:
    """جستجوی شهر (به فارسی یا انگلیسی)"""
    query = query.lower().strip()
    results = {}
    for fa, en in iran_cities.items():
        if query in fa.lower() or query in en.lower():
            results[fa] = {
                "en": en,
                "province": get_province_of_city(fa),
                "coords": get_city_coords(fa),
                "population": get_city_population(fa)
            }
    return results

